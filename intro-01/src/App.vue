<template>
  <section class="block">
    <input type="text" v-on:input="chgMessage">
    <p>{{ msg }}</p>
  </section>
</template>

<script>
export default {
  name: 'app',
  data () {
    return {
      msg: 'Welcome to Your Vue.js App'
    }
  },
  methods: {
    chgMessage ( event ) {
      this.msg = event.target.value;
    }
  }
}
</script>

